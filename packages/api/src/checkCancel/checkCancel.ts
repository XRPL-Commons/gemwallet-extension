import {
  CheckCancelRequest,
  CheckCancelResponse,
  GEM_WALLET,
  RequestCheckCancelMessage,
  ResponseType
} from '@gemwallet/constants';

import { deserializeError } from '../helpers/errors';
import { sendMessageToContentScript } from '../helpers/extensionMessaging';

export const checkCancel = async (payload: CheckCancelRequest): Promise<CheckCancelResponse> => {
  /* response:
   * if the transaction succeeds:
   * - type: 'response'
   * - result:
   *    - hash: transaction hash
   *
   * if the user rejects the transaction:
   * - type: 'reject'
   * - result: undefined
   *
   * if the transaction fails:
   * - throw an error
   */
  let response: CheckCancelResponse = {
    type: ResponseType.Reject,
    result: undefined
  };

  try {
    const message: RequestCheckCancelMessage = {
      app: GEM_WALLET,
      type: 'REQUEST_CHECK_CANCEL/V3',
      payload
    };
    const { result, error } = await sendMessageToContentScript(message);
    const parsedError = error ? deserializeError(error) : undefined;
    if (parsedError) {
      throw parsedError;
    }

    if (result) {
      response.type = ResponseType.Response;
      response.result = result;
    }
  } catch (e) {
    throw e;
  }
  return response;
};
